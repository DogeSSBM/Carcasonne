Color rgbToColor(const uint R, const uint G, const uint B)
{
	Color C = {R, G, B, 0xFF};
	return C;
}

Color rgbaToColor(const uint R, const uint G, const uint B, const uint A)
{
	Color C = {R, G, B, A};
	return C;
}

